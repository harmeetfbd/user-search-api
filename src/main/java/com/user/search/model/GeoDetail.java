package com.user.search.model;


public class GeoDetail {

    private String lat;

    private String lng;

    public String getLat() {
        return lat;
    }

    public GeoDetail setLat(String lat) {
        this.lat = lat;
        return this;
    }

    public String getLng() {
        return lng;
    }

    public GeoDetail setLng(String lng) {
        this.lng = lng;
        return this;
    }
}
